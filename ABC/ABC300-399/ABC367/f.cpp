#include <random>

int main(){
    int n,q;
    cin >> n >> q;
    vector<int> a(n);
    cin >> a;
    vector<int> b(n);
    cin >> b;
    // debug(a);
    // debug(b);
    map<ll,ll> hash_idx;
    rep(i,n){
        if(hash_idx.count(a[i])){
            continue;
        }
        hash_idx[a[i]] = ll(rand())*ll(rand());
    }
    rep(i,n){
        if(hash_idx.count(b[i])){
            continue;
        }
        hash_idx[a[i]] = ll(rand())*ll(rand());
    }
    // debug(hash_idx);
    vector<ll> za(n),zb(n);
    rep(i,n){
        za[i] = hash_idx[a[i]];
        zb[i] = hash_idx[b[i]];
    }
    // debug(za);
    // debug(zb);
    segtree<ll,op,e> seg_a(za);
    segtree<ll,op,e> seg_b(zb);
    while(q--){
        ll L,R,LL,RR;
        cin >> L >> R >> LL >> RR;
        if(R-L != RR-LL){
            cout << "No" << endl;
            continue;
        }
        L--;
        LL--;
        ll hash_a = seg_a.prod(L,R);
        ll hash_b = seg_b.prod(LL,RR);
        // debug(hash_a);
        // debug(hash_b);
        if(hash_a == hash_b){
            cout << "Yes" << endl;
        }else{
            cout << "No" << endl;
        }
    }
}