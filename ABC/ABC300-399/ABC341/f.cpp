int main()
{
    int n, m;
    cin >> n >> m;
    vector<vector<int>> to(n, vector<int>());
    vector<pair<int, int>> node(n);
    rep(i, n)
    {
        node[i].second = i;
    }
    rep(i, m)
    {
        int u, v;
        cin >> u >> v;
        u--;
        v--;
        to[u].push_back(v);
        to[v].push_back(u);
        node[u].first++;
        node[v].first++;
    }
    vector<int> w(n), a(n);
    cin >> w >> a;
    sort(all(node));
    debug(node);
    queue<int> q;
    rep(i, n)
    {
        if (node[i].first == 1)
        {
            q.push(node[i].second);
        }
    }
    debug(q);
    vector<int> dp(n);
    while (q.size())
    {
        int now = q.front();
        q.pop();
        vector<int> lower_node;
        for (int next : to[now])
        {
            if (w[now] > w[next])
            {
                lower_node.push_back(next);
            }else if(w[now] < w[next]){
                q.push(next);
            }
        }
        int p = lower_node.size();
        vector<vector<int>> ep(p + 1, vector<int>(w[now]));
        rep(i, p)
        {
            rep(j, w[now])
            {
                auto chmax = [](auto &a, auto b)
                { a = max(a, b); };
                chmax(ep[i + 1][j], ep[i][j]);
                int next_j = j + w[lower_node[i]];
                if (next_j < w[now])
                {
                    chmax(ep[i + 1][next_j], ep[i][j] + 1);
                }
            }
        }
        cout << ep << endl;
        dp[now] = ep[p][w[now] - 1] + 1;
    }
    cout << dp << endl;
}