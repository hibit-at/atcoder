int main()
{
    int n, m, k;
    cin >> n >> m >> k;
    vector<vector<int>> to(n, vector<int>());
    rep(i, m)
    {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        to[a].push_back(b);
        to[b].push_back(a);
    }
    // vector<bool> guard(n);
    vector<pair<int, int>> PH(k);
    map<int, int> memo;
    rep(i, k)
    {
        int p, h;
        cin >> p >> h;
        p--;
        PH[i] = {h, p};
        memo[p] = h;
    }
    sort(all(PH));
    reverse(all(PH));
    // debug(PH);
    priority_queue<pair<int, int>> q;
    rep(i, k)
    {
        q.push(PH[i]);
    }
    // debug(q);
    vector<bool> seen(n);
    while (q.size())
    {
        auto [now_h, now_p] = q.top();
        q.pop();
        if (seen[now_p])
        {
            continue;
        }
        seen[now_p] = true;
        if (now_h > 0)
        {
            for (auto next : to[now_p])
            {
                q.push({now_h - 1, next});
            }
        }
    }
    // debug(seen);
    vector<int> ans;
    rep(i, n)
    {
        if (seen[i])
        {
            ans.push_back(i + 1);
        }
    }
    cout << ans.size() << endl;
    cout << ans << endl;
}