#include <atcoder/dsu>
using namespace atcoder;

int main()
{
    int n, m;
    cin >> n >> m;
    set<pair<ll, ll>> st;
    rep(i, n)
    {
        ll a;
        cin >> a;
        st.insert({a, i + 1});
    }
    // cout << sst << endl;
    while (m--)
    {
        int uf_size = st.size();
        dsu uf(uf_size + 1);
        set<pair<ll, ll>> alter;
        for (auto [a, b] : st)
        {
            alter.insert({a + b, b});
            if (a + b >= 0 && a + b < uf_size)
            {
                uf.merge(a + b + 1, a + b);
            }
        }
        int ans = uf.size(0);
        // debug(uf.size(0));
        while (alter.lower_bound({ans,0}) != alter.end())
        {
            alter.erase(alter.lower_bound({ans,0}));
        }
        // debug(alter);
        swap(st, alter);
        cout << ans - 1 << endl;
    }
}